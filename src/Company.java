public class Company {
	
		//Company Attributes 
		private int id;
		private int shares;
		private int price;
		
		//Getters and Setters 
		public int getId() {
			return id;
		}

		public void setId(int id) {
			this.id = id;
		}

		public int getShares() {
			return shares;
		}

		public void setShares(int shares) {
			this.shares = shares;
		}

		public int getPrice() {
			return price;
		}

		public void setPrice(int price) {
			this.price = price;
		}

	}

	

